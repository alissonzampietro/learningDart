void main() {
    /* 
     * Number to make operations
     */
    int a = 20;
    int b = 7;

    /* 
     * Concatening values (two ways)
     */
    var name = 'Alisson';
    print("Olá meu nome é "+ name);
    print("Olá meu nome é $name");
    print("Olá meu nome é ${name}");

    /* 
     * Sum numbers
     */
    var sum = a + b;
    print(sum);

    /* 
     * Subtract numbers
     */
    var sub = a + b;
    print(sub);

    /* 
     * Bigger than
     */
    print(a > b);

    /* 
     * Smaller than
     */
    print(a < b);

    /* 
     * Multiply
     */
    print(a * b);

    /* 
     * Different
     */
    print(a != b);
}